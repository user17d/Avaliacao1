public class Triangulo {
    private double Base;
    private double Altura;
    public double calcArea(){
        return (Base * Altura /2);
    }

    public double getBase() {
        return Base;
    }

    public void setBase(double base) {
        this.Base = base;
    }

    public double getAltura() {
        return Altura;
    }

    public void setAltura(double altura) {
        this.Altura = altura;
    }


    }


